function [y] = beale(X, Y)

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% BEALE FUNCTION
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


term1 = (1.5 - X + X.*Y)^2;
term2 = (2.25 - X + X.*Y.^2).^2;
term3 = (2.625 - X + X.*Y.^3).^2;

y = term1 + term2 + term3;

end
